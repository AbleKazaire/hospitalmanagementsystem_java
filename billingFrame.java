    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         Date selectedDate = dateDate.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Please select a payment date.");
            return;
        }

        java.sql.Date sqlDate = new java.sql.Date(selectedDate.getTime());

        try (Connection con = DriverManager.getConnection(url, user, pass)) {
            String sql = "UPDATE billing SET patient_id=?, total_amount=?, bill_date=?, payment_status=? WHERE bill_id=?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setInt(1, Integer.parseInt(txtPatientid.getText().trim()));
            pst.setBigDecimal(2, new java.math.BigDecimal(txtAmountT.getText().trim()));
            pst.setDate(3, sqlDate);
            pst.setInt(4, Integer.parseInt(txtBillid.getText().trim()));
             pst.setString(5, comboStatus.getSelectedItem().toString());
            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Bill updated successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "No bill found with that ID.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try (Connection con = DriverManager.getConnection(url, user, pass)) {
            String sql = "SELECT * FROM billing WHERE bill_id=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, txtBillid.getText());

            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txtPatientid.setText(rs.getString("patient_id"));
                txtAmountT.setText(rs.getString("amount"));
                dateDate.setDate(rs.getDate("payment_date"));
            } else {
                JOptionPane.showMessageDialog(this, "No bill found with that ID.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

