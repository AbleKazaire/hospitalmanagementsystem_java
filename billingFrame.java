    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Date selectedDate = dateDate.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Please select a payment date.");
            return;
        }

        java.sql.Date sqlDate = new java.sql.Date(selectedDate.getTime());

        try (Connection con = DriverManager.getConnection(url, user, pass)) {
            String sql = "INSERT INTO billing (patient_id, total_amount, bill_date, payment_status) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setInt(1, Integer.parseInt(txtPatientid.getText().trim()));
            pst.setBigDecimal(2, new java.math.BigDecimal(txtAmountT.getText().trim()));
            pst.setDate(3, sqlDate);
            pst.setString(4, comboStatus.getSelectedItem().toString());
            
            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Bill added successfully.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

